--recursive quicksort function to sort list of numbers
qsort [] = []
qsort (pivot:n) = (qsort less_than_arr) ++ (pivot : qsort greater_than_arr)
   where less_than_arr = [x | x <- n, x <= pivot]
         greater_than_arr = [x | x <- n, x > pivot]

--main function that runs 10 test cases 
main = do
       putStrLn("1.The unsorted sequence is:")
